package Ops;

/**
 * @author Kris
 */
public class Operations {

    public static int Addition(int num1, int num2){
        int sum;

        sum = num1 + num2;

        return sum;
    }
    
    public static int Substract(int num1, int num2){
        int substract;

        substract = num1 - num2;

        return substract;
    }

    public static int Multiply(int num1, int num2){
        int product;

        product = num1 * num2;

        return product;
    }

    public static int Divide(int num1, int num2){
        int product;
        product = num1 / num2;
        return product;
    }

}
